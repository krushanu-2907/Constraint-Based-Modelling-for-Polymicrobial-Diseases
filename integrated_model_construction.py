def get_base_id(met_id):
  # Extract metabolite base ID without model suffix
  # For example, glc_D_[e]_etec → glc_D_[e]
  if met_id.endswith("ent"):
    s = met_id.replace("_ent", "")
  elif met_id.endswith("etec"):
    s = met_id.replace("_etec", "")
  else:
    s = met_id.replace("_vc", "")
  return s

def is_extracellular(met):
  if "[u]_ent" in met.id:
    return True
  elif "[e]" in met.id and "ent" not in met.id:
    return True
  else:
    return False

def is_periplasm(met):
  return "[p]" in met.id

def is_cytoplasm(met):
  return "[c]" in met.id

# ------------------------------------------
# Step 1: Rename metabolites with model tags
# ------------------------------------------

def rename_model(model, tag):
    model = model.copy()

    for met in model.metabolites:
        met.id = f"{met.id}_{tag}"

    for rxn in model.reactions:
        rxn.id = f"{rxn.id}_{tag}"

    for gene in model.genes:
        gene.id = f"{gene.id}_{tag}"

    return model

# ------------------------------------------------------------------------------------
# Step 2: Rename extracellular metabolites as shared metabolites (from enterocyte only)
# -------------------------------------------------------------------------------------

def create_shared_from_ent(ent_model):
  shared_dict = {}

  for met in ent_model.metabolites:
    if is_extracellular(met):
      base_id = get_base_id(met.id)
      shared_id = f"{base_id}_shared"
      shared_dict[shared_id] = Metabolite(shared_id, name = met.name, compartment = "e_shared", formula = met.formula)

  return shared_dict

# Metabolite names of the shared metabolites stored in a dictionary for later comparison
def get_allowed_bases(ent_model):
  dict_m = {}
  for m in ent_model.metabolites:
   if is_extracellular(m):
    s = m.name
    dict_m[m] = s.lower()
  return dict_m

# Helper functions for matching metabolite IDs based on their base names
pattern = r"[A-Za-z]"

def extract(s):
    s = s.split("[")[0]      # remove compartment
    s = s.replace("-", "")   # remove hyphen
    return re.findall(pattern, s)

def match(s1, s2):
    return set(extract(s1)) == set(extract(s2))

