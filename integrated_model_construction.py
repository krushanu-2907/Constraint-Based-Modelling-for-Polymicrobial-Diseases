def combine_models(models, shared_dict):
    combined = cobra.Model("multi_cell_system")

    # Step 1: Add all metabolites first
    for model in models:
        for met in model.metabolites:
            if met.id not in combined.metabolites:
                combined.add_metabolites([met.copy()])

    # Step 3: Add reactions
    for model in models:
        for rxn in model.reactions:

            if rxn.id.startswith("EX_"):
                continue

            new_rxn = Reaction(rxn.id)
            new_rxn.name = rxn.name
            new_rxn.lower_bound = rxn.lower_bound
            new_rxn.upper_bound = rxn.upper_bound
            new_rxn.gene_reaction_rule = rxn.gene_reaction_rule

            # Map metabolites to combined model objects
            mets = {}
            for met, coeff in rxn.metabolites.items():
                mets[combined.metabolites.get_by_id(met.id)] = coeff

            new_rxn.add_metabolites(mets)

            combined.add_reactions([new_rxn])

    # Step 2: Replace extracellular metabolites with shared ones
    for met in shared_dict.values():
      base = get_base_id(met.id)
      shared_met_id = f"{base}".split("[")[0] + "_shared"
      name = met.name
      new_met = Metabolite(shared_met_id, name = met.name, compartment = "shared", formula = met.formula)

      for m in combined.metabolites:
          if match(m.id, met.id) and is_extracellular(m):
            for r in m.reactions:
              coeff = r.get_coefficient(m)
              r.add_metabolites({new_met: coeff})
              r.add_metabolites({m: 0}, combine = False)

      ex_rxn_shared = Reaction(f"EX_{new_met.name.replace(" ", "")}")
      ex_rxn_shared.name = f"{new_met.name} Exchange - Intestine"
      ex_rxn_shared.add_metabolites({new_met: -1})
      ex_rxn_shared.lower_bound = -100
      ex_rxn_shared.upper_bound = 10
      combined.add_reactions([ex_rxn_shared])

      for model in models[1:]:
        for m in model.metabolites:

          if match(m.id, met.id) and is_periplasm(m):
            rxn_transport = Reaction(f"{m.id.replace(" ", "")}_transport")
            rxn_transport.name = f"{met.name} Transport"
            rxn_transport.add_metabolites({new_met: -1, m: 1})
            rxn_transport.lower_bound = -100
            rxn_transport.upper_bound = 100
            combined.add_reactions([rxn_transport])

      for m in models[0].metabolites:
        if match(m.id, met.id) and is_cytoplasm(m):
          rxn_transport = Reaction(f"{m.id.replace(" ", "")}_transport")
          rxn_transport.name = f"{met.name} Transport"
          rxn_transport.add_metabolites({new_met: -1, m: 1})
          rxn_transport.lower_bound = -100
          rxn_transport.upper_bound = 100
          combined.add_reactions([rxn_transport])

    # Step 4: Add genes (optional but cleaner)
    for model in models:
        for gene in model.genes:
            if gene.id not in combined.genes:
                combined.genes += [gene.copy()]

    return combined

# Function to add exchange reactions for bloodstream metabolites
def add_exchanges_bloodstream(combined, ent):
  for r in ent.reactions:
    if r.id.startswith("EX_"):
      m = list(r.metabolites.keys())[0]
      for met in combined.metabolites:
        if match(m.id, met.id) and met.compartment == "u":
          ex_rxn_u = Reaction(f"EX_{met.id.replace(" ", "")}_[u]")
          ex_rxn_u.name = f"{met.name} Exchange - Bloodstream"
          ex_rxn_u.add_metabolites({met: 1})
          ex_rxn_u.lower_bound = r.lower_bound
          ex_rxn_u.upper_bound = r.upper_bound
          if ex_rxn_u not in combined.reactions:
            combined.add_reactions([ex_rxn_u])

# Function to add dilution reactions for extracellular metabolites
def add_dilution(combined, list_models, shared_dict):
  for model in list_models:
    for m in model.metabolites: # Iterate over all metabolites
      if is_extracellular(m): # Then apply the condition
        r = Reaction(f"{m.id.split("[ ")[0]}_dilution")
        r.name = f"{m.name} Dilution"
        r.add_metabolites({m: -1})
        r.lower_bound = -10
        r.upper_bound = 10
        if r not in combined.reactions:
          combined.add_reactions([r])

