# Model Integration
# -----------------

def integrate_models(Vc, ETEC, ent):

  Vc = rename_model(Vc, "vc")
  ETEC = rename_model(ETEC, "etec")
  ent = rename_model(ent, "ent")

  shared_dict = create_shared_from_ent(ent)
  allowed_bases = get_allowed_bases(ent)

  combined = combine_models([ent, Vc, ETEC], shared_dict)
  add_exchanges_bloodstream(combined, ent)

  add_dilution(combined, [Vc, ETEC], shared_dict)

  return combined

